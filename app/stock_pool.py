def get_all_stocks():
    """获取所有A股代码"""
    all_stocks = []
    for category, stocks in A_STOCK_POOL.items():
        all_stocks.extend(stocks)
    return list(set(all_stocks))  # 去重


def get_stock_category(ticker):
    """获取股票所属细分板块"""
    for category, stocks in A_STOCK_POOL.items():
        if ticker in stocks:
            return category
    return "其他"


def get_major_sector(sub_sector):
    """获取大板块名称"""
    return SECTOR_MAPPING.get(sub_sector, "其他")

