# ═══════════════════════════════════════════════════════════════
# 高碰撞 token 黑名单（做 contains 匹配时禁止单独使用这些词）
# ═══════════════════════════════════════════════════════════════
# 这些 token 在证监会分类里被很多行业共享，单独用来 contains 会误匹配
# （例如"工业"同时存在于农副食品加工业、石油煤炭加工业、黑色金属加工业、
# 有色金属加工业 4 个行业名里；"制造"存在于 30+ 行业）
HIGH_COLLISION_TOKENS: set[str] = {
    "工业", "加工", "制造", "服务", "生产", "供应", "设备", "制品",
    "其他", "专业", "业", "业务",
}


def _first_meaningful_prefix(s: str, n: int = 2) -> str:
    """Return the first n non-blacklisted chars of s. Avoids using
    '工业' or '加工' as the matching prefix."""
    if not s:
        return ""
    # 如果 s 直接就是高碰撞词（如 "工业" 本身），返回空强制走 mapping
    if s[:n] in HIGH_COLLISION_TOKENS:
        # 跳过前缀，取 n+2 位
        return s[n:n + 2] if len(s) >= n + 2 else ""
    return s[:n]


def resolve_csrc_industry(sw_industry: str, df):
    """从 cninfo 证监会行业分类 DataFrame 里找匹配 sw_industry 的那一行。

    优先级：
      1. SW_TO_CSRC_INDUSTRY 硬映射（精确对应）
      2. 申万行业作为整体字符串包含在证监会行业名称里
      3. 去掉高碰撞前缀后做 contains fuzzy
      4. 找不到返回 None（**绝不**盲选 iloc[0]）

    参数：
      sw_industry: 申万三级行业名 e.g. "工业金属" / "白酒" / "光学光电子"
      df: akshare `stock_industry_pe_ratio_cninfo` 返回的 DataFrame

    返回：
      pandas.Series（匹配到的 df 行），或 None（未匹配，**不要**用 iloc[0] 兜底）
    """
    if df is None or not hasattr(df, "empty") or df.empty:
        return None
    if not sw_industry:
        return None
    if "行业名称" not in df.columns:
        return None

    names = df["行业名称"].astype(str)

    # ── 策略 1: 硬映射 ──
    target_csrc = SW_TO_CSRC_INDUSTRY.get(sw_industry)
    if target_csrc:
        matches = df[names == target_csrc]
        if not matches.empty:
            return matches.iloc[0]
        # 硬映射表里的目标名在这份 df 里找不到（不太可能，但做兜底）
        # 用 contains 找最接近的
        matches = df[names.str.contains(target_csrc[:4], na=False)]
        if not matches.empty:
            return matches.iloc[0]

    # ── 策略 2: 申万名整体作为子串 ──
    matches = df[names.str.contains(sw_industry, na=False)]
    if not matches.empty:
        return matches.iloc[0]

    # ── 策略 3: 去掉高碰撞前缀后 fuzzy ──
    safe_prefix = _first_meaningful_prefix(sw_industry, n=2)
    if safe_prefix and safe_prefix not in HIGH_COLLISION_TOKENS:
        matches = df[names.str.contains(safe_prefix, na=False)]
        if not matches.empty:
            return matches.iloc[0]

    # ── 策略 4: 全挂 —— 返 None，绝不盲选 iloc[0] ──
    return None


