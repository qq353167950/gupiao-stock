"""申万行业 → 证监会行业分类 语义映射 · v2.8.3

## Why this module exists

cninfo `stock_industry_pe_ratio_cninfo` 返回的是**证监会行业分类**（120 个大类），
而我们 `fetch_basic` 拿到的 `industry` 字段是**申万三级行业**（~240 个细分）。
两套分类体系完全不同，不能做字面匹配。

v2.8.2 及之前的代码偷懒用 `df["行业名称"].str.contains(industry_name[:2])` 做
模糊匹配，会触发严重 bug：

    "工业金属" (申万) → str.contains("工业") 命中 4 个：
      农副食品加工业  ← iloc[0] 选中，完全错误
      石油、煤炭及其他燃料加工业
      黑色金属冶炼和压延加工业
      有色金属冶炼和压延加工业  ← 实际正确

所有包含"工业""加工业""制造"字样 的申万行业都会被误分到农副食品加工业。
**云铝股份 (000807.SZ) 被归类为农副食品加工** 就是这个 bug 的典型症状。

## Fix strategy

1. `SW_TO_CSRC_INDUSTRY` 硬映射表，覆盖常见申万三级行业 → 证监会行业
2. `resolve_csrc_industry()` 智能解析：
   - 第一优先：硬映射表精确命中
   - 第二优先：申万行业名称作为子串整体包含在某个证监会行业里
   - 第三优先：按"关键 token"做加权评分（尾字通常是最具辨识度的类别词）
   - 第四优先：首 2 字 fuzzy（保留原有行为做最后兜底，但必须排除通用前缀
     "工业""加工""制造"等高碰撞词）

## Usage

    from lib.industry_mapping import resolve_csrc_industry
    row = resolve_csrc_industry("工业金属", df)   # df 是 cninfo 返回的 DataFrame
    # row 是 pandas Series (完整行) 或 None

"""
